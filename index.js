let passwordOneEl = document.querySelector(".password-one");
let passwordTwoEl = document.querySelector(".password-two");
let length = 15;

function generateRandomSign() {
  let randomSign = Math.floor(Math.random() * 91) + 1;
  return characters[randomSign];
}

function generateRandomPassword() {
  let randomPassword = "";
  for (let i = 0; i < length; i++) {
    randomPassword += generateRandomSign();
  }
  return randomPassword;
}
const passwordOne = generateRandomPassword();
const passwordTwo = generateRandomPassword();
function generatePassword() {
  passwordOneEl.textContent += passwordOne;
  passwordTwoEl.textContent += passwordTwo;
}
